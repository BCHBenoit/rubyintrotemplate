# ruby 3-arrays.rb

# Arrays are lists of things
favourite_foods = ["tacos", "pizza", "bourbon"]
puts favourite_foods

# Accessing the array
puts favourite_foods[0]

# Add to the array
favourite_foods = favourite_foods + ["icecream", "tortas"]
favourite_foods.push("burritos")
favourite_foods << "burgers"
puts favourite_foods
